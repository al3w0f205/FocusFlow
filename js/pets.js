class PetEngine {
    constructor(canvasId) {
        this.canvas = document.getElementById(canvasId);
        if (!this.canvas) return;
        this.ctx = this.canvas.getContext('2d');
        this.gridSize = 16;
        this.pixelSize = this.canvas.width / this.gridSize;
        
        this.currentPet = 'cat';
        this.currentState = 'idle';
        this.frameIndex = 0;
        this.lastFrameTime = 0;
        
        this.fps = {
            idle: 1, // very slow blink
            focus: 2, // bop
            break: 1
        };

        this.animate = this.animate.bind(this);
        requestAnimationFrame(this.animate);
    }

    setPet(petId) {
        if (SPRITES[petId]) {
            this.currentPet = petId;
            this.frameIndex = 0;
            this.drawFrame();
        }
    }

    setState(state) {
        if (['idle', 'focus', 'break'].includes(state)) {
            if (this.currentState !== state) {
                this.currentState = state;
                this.frameIndex = 0;
                this.drawFrame();
            }
        }
    }

    drawFrame() {
        const frames = SPRITES[this.currentPet][this.currentState];
        if (!frames || frames.length === 0) return;
        
        const frame = frames[this.frameIndex % frames.length];
        this.ctx.clearRect(0, 0, this.canvas.width, this.canvas.height);

        for (let y = 0; y < this.gridSize; y++) {
            for (let x = 0; x < this.gridSize; x++) {
                const colorCode = frame[y][x];
                if (colorCode !== '0') {
                    this.ctx.fillStyle = PALETTE[colorCode];
                    this.ctx.fillRect(x * this.pixelSize, y * this.pixelSize, this.pixelSize, this.pixelSize);
                }
            }
        }
    }

    animate(timestamp) {
        const fps = this.fps[this.currentState] || 2;
        const frameDuration = 1000 / fps;

        if (timestamp - this.lastFrameTime > frameDuration) {
            const frames = SPRITES[this.currentPet][this.currentState];
            if (frames.length > 1) {
                this.frameIndex = (this.frameIndex + 1) % frames.length;
            }
            this.drawFrame();
            this.lastFrameTime = timestamp;
        }

        requestAnimationFrame(this.animate);
    }
}
